class Solution {
  public:
    vector<int> changeBits(int n) {
        // code here
                int bitCount = log2(n) + 1;
        int bitSum = pow(2, bitCount) - 1;
        int firstNo = bitSum - n;
        int secondNo = bitSum;
        return {firstNo, secondNo};
    }
};

